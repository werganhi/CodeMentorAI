def set_run_font(run, size=14, bold=False, italic=False, name="Times New Roman"):
    run.font.name = name
    run._element.rPr.rFonts.set(qn("w:eastAsia"), name)
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.italic = italic


def add_page_number(paragraph):
    paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = paragraph.add_run()
    begin = OxmlElement("w:fldChar")
    begin.set(qn("w:fldCharType"), "begin")
    instr = OxmlElement("w:instrText")
    instr.set(qn("xml:space"), "preserve")
    instr.text = "PAGE"
    end = OxmlElement("w:fldChar")
    end.set(qn("w:fldCharType"), "end")
    run._r.append(begin)
    run._r.append(instr)
    run._r.append(end)
    set_run_font(run, 12)


def style_doc(doc):
    section = doc.sections[0]
    section.top_margin = Cm(2)
    section.bottom_margin = Cm(2)
    section.left_margin = Cm(3)
    section.right_margin = Cm(1.5)
    add_page_number(section.footer.paragraphs[0])

    for style_name in ["Normal", "Heading 1", "Heading 2", "Heading 3"]:
        style = doc.styles[style_name]
        style.font.name = "Times New Roman"
        style._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
        style.font.size = Pt(14)
        style.paragraph_format.line_spacing = 1.5
        style.paragraph_format.space_after = Pt(0)
        style.paragraph_format.space_before = Pt(0)
    doc.styles["Normal"].paragraph_format.first_line_indent = Cm(1.25)

    if "CodeBlock" not in [style.name for style in doc.styles]:
        code = doc.styles.add_style("CodeBlock", WD_STYLE_TYPE.PARAGRAPH)
        code.font.name = "Consolas"
        code._element.rPr.rFonts.set(qn("w:eastAsia"), "Consolas")
        code.font.size = Pt(9)
        code.paragraph_format.line_spacing = 1.0
        code.paragraph_format.space_after = Pt(6)
        code.paragraph_format.first_line_indent = Cm(0)


def paragraph(
    doc,
    text="",
    align=WD_ALIGN_PARAGRAPH.JUSTIFY,
    indent=True,
    bold=False,
    italic=False,
    size=14,
):
    p = doc.add_paragraph()
    p.alignment = align
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.space_after = Pt(0)
    p.paragraph_format.first_line_indent = Cm(1.25) if indent else Cm(0)
    run = p.add_run(text)
    set_run_font(run, size=size, bold=bold, italic=italic)
    return p


def center(doc, text="", bold=False, size=14):
    return paragraph(
        doc, text, align=WD_ALIGN_PARAGRAPH.CENTER, indent=False, bold=bold, size=size
    )


def h1(doc, text):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(6)
    run = p.add_run(text.upper())
    set_run_font(run, 14, True)


def h2(doc, text):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.space_before = Pt(8)
    p.paragraph_format.space_after = Pt(3)
    run = p.add_run(text)
    set_run_font(run, 14, True)


def bullet(doc, text):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    p.paragraph_format.left_indent = Cm(1.25)
    p.paragraph_format.first_line_indent = Cm(0)
    p.paragraph_format.line_spacing = 1.5
    run = p.add_run("- " + text)
    set_run_font(run, 14)


def numbered(doc, number, text):
    paragraph(doc, f"{number}. {text}", indent=False)


def codeblock(doc, text):
    for line in text.strip("\n").split("\n"):
        p = doc.add_paragraph(style="CodeBlock")
        run = p.add_run(line)
        set_run_font(run, 9, name="Consolas")


def cell_text(cell, text, bold=False, size=12):
    cell.vertical_alignment = WD_CELL_VERTICAL_ALIGNMENT.CENTER
    cell.text = ""
    p = cell.paragraphs[0]
    p.paragraph_format.first_line_indent = Cm(0)
    p.paragraph_format.line_spacing = 1.0
    run = p.add_run(str(text))
    set_run_font(run, size, bold)


def table(doc, caption, headers, rows):
    paragraph(doc, caption, indent=False)
    tbl = doc.add_table(rows=1, cols=len(headers))
    tbl.style = "Table Grid"
    tbl.alignment = WD_TABLE_ALIGNMENT.CENTER
    for index, header in enumerate(headers):
        cell_text(tbl.rows[0].cells[index], header, True)
    for row in rows:
        cells = tbl.add_row().cells
        for index, value in enumerate(row):
            cell_text(cells[index], value)
    paragraph(doc, "", indent=False)


def picture(doc, path, caption, width=2.45):
    paragraph(doc, caption, align=WD_ALIGN_PARAGRAPH.CENTER, indent=False, italic=True)
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run()
    run.add_picture(str(path), width=Inches(width))


def toc_line(doc, title, page):
    dots = "." * max(4, 92 - len(title) - len(str(page)))
    paragraph(doc, f"{title}{dots}{page}", WD_ALIGN_PARAGRAPH.LEFT, indent=False)


def page_break(doc):
    doc.add_page_break()


def safe_snippet(path, start, end):
    lines = Path(path).read_text(encoding="utf-8").splitlines()
    return "\n".join(
        f"{line_number + 1}: {lines[line_number]}"
        for line_number in range(start - 1, min(end, len(lines)))
    )


